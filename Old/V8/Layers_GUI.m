%{
if ~isfield(handles.scale,'choice')
    handles.scale.choice(1) = 0;
    handles.scale.choice(2) = 0;
    Layers(handles.foldername,handles.pars,handles.numoflayers,handles.layerwidth,handles.scale);
elseif handles.scale.choice(1)==[]|handles.scale.choice(2)==[]
    if handles.scale.choice(1) == []
        handles.scale.choice(1) = 0;
        Layers(handles.foldername,handles.pars,handles.numoflayers,handles.layerwidth,handles.scale);
    else
        handles.scale.choice(2) = 0;
        Layers(handles.foldername,handles.pars,handles.numoflayers,handles.layerwidth,handles.scale);
    end
else Layers(handles.foldername,handles.pars,handles.numoflayers,handles.layerwidth,handles.scale);
end
%}
Layers(handles.foldername,handles.pars,handles.numoflayers,handles.layerwidth,handles.scale);
    

